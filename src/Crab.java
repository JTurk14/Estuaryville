public class Crab{
	int crabXPos;
	int crabYPos;
	
	public Crab(int x, int y) {
		this.crabXPos = x;
		this.crabYPos = y;
	}
	
	public int getCrabXPos() {
		return crabXPos;
	}

	public void setCrabXPos(int crabXPos) {
		this.crabXPos = crabXPos;
	}

	public int getCrabYPos() {
		return crabYPos;
	}

	public void setCrabYPos(int crabYPos) {
		this.crabYPos = crabYPos;
	}
	
}